	public function getOrders($data = array()) {
		 
				$sql = "SELECT o.order_id, o.firstname, o.lastname, o.telephone, o.shipping_city, o.shipping_address_1, o.email, o.comment_manager, o.calculated_summ, o.manager_process_orders,  o.delivery_price, o.build_price, o.build_price_yes_no, o.build_price_prefix, o.rise_product_price, o.rise_product_yes_no, o.rise_product_price_prefix, o.payment_method, o.shipping_method, o.text_ttn, CONCAT(o.firstname, ' ', o.lastname) AS customer,(SELECT os.row_color FROM " . DB_PREFIX . "order_status os WHERE os.order_status_id = o.order_status_id AND os.language_id = '" . (int)$this->config->get('config_language_id') . "') AS row_color, o.order_status_id,(SELECT os.row_color_text FROM " . DB_PREFIX . "order_status os WHERE os.order_status_id = o.order_status_id AND os.language_id = '" . (int)$this->config->get('config_language_id') . "') AS row_color_text, (SELECT os.name FROM " . DB_PREFIX . "order_status os WHERE os.order_status_id = o.order_status_id AND os.language_id = '" . (int)$this->config->get('config_language_id') . "') AS status, o.shipping_code, o.total, o.currency_code, o.currency_value, o.date_added, o.date_modified FROM `" . DB_PREFIX . "order` o";
		

		if (isset($data['filter_order_status'])) {
			$implode = array();

			$order_statuses = explode(',', $data['filter_order_status']);

			foreach ($order_statuses as $order_status_id) {
				$implode[] = "o.order_status_id = '" . (int)$order_status_id . "'";
			}

			if ($implode) {
				$sql .= " WHERE (" . implode(" OR ", $implode) . ")";
			}
		} else {
			$sql .= " WHERE o.order_status_id > '0'";
		}


		if (!empty($data['filter_build_price_yes_no'])) {
			$sql .= " AND o.build_price_yes_no = '" . $data['filter_build_price_yes_no'] . "'";
		}
		if (!empty($data['filter_rise_product_yes_no'])) {
			$sql .= " AND o.rise_product_yes_no = '" . $data['filter_rise_product_yes_no'] . "'";
		}
		if (!empty($data['filter_delivery_price'])) {
			$sql .= " AND o.delivery_price = '" . $data['filter_delivery_price'] . "'";
		}
		if (!empty($data['filter_calculated_summ'])) {
			$sql .= " AND o.calculated_summ = '" . $data['filter_calculated_summ'] . "'";
		}
		if (!empty($data['filter_manager_process_orders'])) {
			$sql .= " AND o.manager_process_orders = '" . $data['filter_manager_process_orders'] . "'";
		}
		if (!empty($data['filter_date_added_start'])) {
			$sql .= " AND DATE(o.date_added) >= DATE('" . $this->db->escape($data['filter_date_added_start']) . "') AND DATE(o.date_added) <=DATE('" . $this->db->escape($data['filter_date_added_end']) . "')";	
		}
		
		if (!empty($data['filter_order_id'])) {
			$sql .= " AND o.order_id = '" . (int)$data['filter_order_id'] . "'";
		}

		if (!empty($data['filter_customer'])) {
			 
			$sql .= " AND CONCAT(o.firstname, ' ', o.lastname, ' ', o.telephone, ' ', o.email, ' ', o.shipping_city, ' ', o.shipping_address_1, ' ', o.payment_method, ' ', o.shipping_method) LIKE '%" . $this->db->escape($data['filter_customer']) . "%'";
		
		}

		if (!empty($data['filter_date_added'])) {
			$sql .= " AND DATE(o.date_added) = DATE('" . $this->db->escape($data['filter_date_added']) . "')";
		}

		if (!empty($data['filter_date_modified'])) {
			$sql .= " AND DATE(o.date_modified) = DATE('" . $this->db->escape($data['filter_date_modified']) . "')";
		}

		if (!empty($data['filter_total'])) {
			$sql .= " AND o.total = '" . (float)$data['filter_total'] . "'";
		}

		$sort_data = array(
			'o.order_id',
			'customer',
			'status',
			'o.date_added',
			'o.date_modified',
			'o.total'
		);

		if (isset($data['sort']) && in_array($data['sort'], $sort_data)) {
			$sql .= " ORDER BY " . $data['sort'];
		} else {
			$sql .= " ORDER BY o.order_id";
		}

		if (isset($data['order']) && ($data['order'] == 'DESC')) {
			$sql .= " DESC";
		} else {
			$sql .= " ASC";
		}

		if (isset($data['start']) || isset($data['limit'])) {
			if ($data['start'] < 0) {
				$data['start'] = 0;
			}

			if ($data['limit'] < 1) {
				$data['limit'] = 20;
			}

			$sql .= " LIMIT " . (int)$data['start'] . "," . (int)$data['limit'];
		}

		$query = $this->db->query($sql);

		return $query->rows;
	}


